import { CHATGPT_API_KEY } from "../env";
import { backendUrl } from "./globals";

function QuizResults({
  code,
  setCode,
  submitted,
  setSubmitted,
  tab,
  setTab,
  chatHistory,
  setChatHistory,
  data,
}) {
  const handleNewQuiz = () => {
    setTab("quiz");
  };
  const handleBrandNewQuiz = () => {
    setSubmitted(false);
    setTab("quiz");
  };
  const [inputMessage, setInputMessage] = useState("");
  const [spinner, setSpinner] = useState(false);

  const sendMessage = async () => {
    if (inputMessage.trim() === "") return;

    setInputMessage("");
    setSpinner(true);
    try {
      const response = await axios.post(
        "https://api.openai.com/v1/chat/completions",
        {
          model: "gpt-3.5-turbo",
          messages: [
            { role: "system", content: "You are a helpful assistant." },
            { role: "user", content: inputMessage },
          ],
        },
        {
          headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${CHATGPT_API_KEY}`,
          },
        }
      );
      const assistantResponse = response.data.choices[0].message;
      setChatHistory([
        ...chatHistory,
        { content: { content: inputMessage, role: "user", isUser: true } },
        {
          content: assistantResponse,
          isUser: false,
        },
      ]);
    } catch (error) {
      console.error("Error sending message to ChatGPT:", error);
    }
    setSpinner(false);
  };
  const transformedData = data.map((item) => {
    const transformedItem = {};
    for (const key in item) {
      if (Object.hasOwnProperty.call(item, key)) {
        transformedItem[key] = item[key] / 5;
      }
    }
    return transformedItem;
  });

  return (
    <View style={styles.quizResultsContainer}>
      <View style={styles.headerWrap}>
        <Text style={styles.headerText}>Results</Text>
      </View>
      <View style={styles.quizResultsContent}>
        {!submitted && (
          <Text style={[styles.topicsTextCode, { marginTop: 20 }]}>
            Please create and submit a survey.
          </Text>
        )}
        {!submitted && (
          <TouchableOpacity
            onPress={() => handleNewQuiz()}
            style={[styles.buttonCreate, { marginTop: 20 }]}
          >
            <Text
              style={{
                color: "white",
                fontFamily: "monospace",
                fontSize: 20,
                padding: 5,
              }}
            >
              New Survey
            </Text>
          </TouchableOpacity>
        )}
        {submitted && (
          <Text style={[styles.topicsTextCode, { marginTop: 20 }]}>
            Results from survey: {code}
          </Text>
        )}
        {submitted && (
          <View style={{ height: 500, width: "100%" }}>
            <ScrollView
              style={[styles.scrollView, { marginBottom: 20, marginTop: 20 }]}
              contentContainerStyle={styles.scrollContainerStyle}
            >
              <RadarChart
                graphSize={400}
                scaleCount={5}
                numberInterval={2}
                data={transformedData}
                options={{
                  graphShape: 1,
                  showAxis: true,
                  showIndicator: true,
                  colorList: ["blue", "red"],
                  dotList: [false, false],
                }}
              />
              <Text
                style={[styles.topicsText, { marginTop: 20, marginBottom: 20 }]}
              >
                Summary of the data generated by ChatGPT.
              </Text>
              <View style={{ width: "100%" }}>
                {chatHistory.map((message, index) => (
                  <View key={index} style={styles.messageContainer}>
                    <Text
                      style={
                        message.content.isUser
                          ? {
                              alignSelf: "flex-end",
                              fontFamily: "monospace",
                              fontSize: 10,
                              marginRight: 10,
                            }
                          : {
                              alignSelf: "flex-start",
                              fontFamily: "monospace",
                              fontSize: 10,
                              marginLeft: 10,
                            }
                      }
                    >
                      {message.content.role}
                    </Text>
                    <Text
                      style={
                        message.content.isUser
                          ? styles.userMessage
                          : styles.assistantMessage
                      }
                    >
                      {message.content.content}
                    </Text>
                  </View>
                ))}
              </View>
              <View style={styles.inputContainer}>
                <TextInput
                  style={styles.input}
                  placeholder="Type your message..."
                  value={inputMessage}
                  onChangeText={setInputMessage}
                />
                {!spinner && (
                  <TouchableOpacity
                    onPress={() => sendMessage()}
                    style={[styles.buttonCreate, { marginBottom: 10 }]}
                  >
                    <Text
                      style={{
                        color: "white",
                        fontFamily: "monospace",
                        fontSize: 16,
                        padding: 1,
                      }}
                    >
                      Send
                    </Text>
                  </TouchableOpacity>
                )}
                {spinner && <ActivityIndicator color="#990030" />}
              </View>
              <TouchableOpacity
                onPress={() => handleBrandNewQuiz()}
                style={styles.buttonCreate}
              >
                <Text
                  style={{
                    color: "white",
                    fontFamily: "monospace",
                    fontSize: 16,
                    padding: 1,
                  }}
                >
                  New Survey
                </Text>
              </TouchableOpacity>
            </ScrollView>
          </View>
        )}
      </View>
    </View>
  );
}

function QuizCreate({
  code,
  setCode,
  submitted,
  setSubmitted,
  chatHistory,
  setChatHistory,
}) {
  const [inputs, setInputs] = useState([""]);

  const addInput = () => {
    setInputs([...inputs, ""]);
  };

  const removeInput = (index) => {
    const newInputs = [...inputs];
    newInputs.splice(index, 1);
    setInputs(newInputs);
  };

  const handleInputChange = (text, index) => {
    const newInputs = [...inputs];
    newInputs[index] = text;
    setInputs(newInputs);
  };
  const handleSubmit = () => {
    const postData = {
      quizCode: code,
      questions: inputs.join(","),
    };
    fetch(
      backendUrl +
        "create-quiz?quizCode=" +
        postData.quizCode +
        "&questions=" +
        postData.questions
    ).catch((e) => {
      console.log("Couldn't find that quiz");
    });
    setSubmitted(true);
  };
  const handleNewQuiz = () => {
    const characters = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
    let randomCode = "";
    for (let i = 0; i < 4; i++) {
      const randomIndex = Math.floor(Math.random() * characters.length);
      randomCode += characters[randomIndex];
    }
    setCode(randomCode);
    setSubmitted(false);
    setInputs([""]);
    setChatHistory([]);
  };
  return (
    <View style={styles.quizCreateContainer}>
      <View style={styles.headerWrap}>
        <Text style={styles.headerText}>Create Survey</Text>
      </View>
      {!submitted && (
        <Text
          style={[styles.topicsTextCode, { marginTop: 20, marginBottom: 20 }]}
        >
          Add your topics:
        </Text>
      )}
      {!submitted && (
        <View style={{ height: 425, width: "100%" }}>
          <ScrollView
            style={[styles.scrollView, { marginBottom: 20 }]}
            contentContainerStyle={styles.scrollContainerStyle}
          >
            {inputs.map((value, index) => (
              <View key={index} style={styles.inputContainer}>
                <TextInput
                  style={styles.input}
                  placeholder={`Topic ${index + 1}`}
                  value={value}
                  onChangeText={(text) => handleInputChange(text, index)}
                />
                {index > 0 && (
                  <TouchableOpacity
                    onPress={() => removeInput(index)}
                    style={styles.buttonCreate}
                  >
                    <Text style={styles.buttonCreateText}>Remove</Text>
                  </TouchableOpacity>
                )}
              </View>
            ))}
            <TouchableOpacity onPress={addInput} style={styles.buttonCreate}>
              <Text style={styles.buttonCreateText}>Add Topic</Text>
            </TouchableOpacity>
          </ScrollView>
        </View>
      )}
      {!submitted && (
        <TouchableOpacity
          onPress={() => handleSubmit(inputs)}
          style={styles.buttonCreate}
        >
          <Text style={styles.buttonCreateText}>Submit</Text>
        </TouchableOpacity>
      )}
      {submitted && (
        <Text style={[styles.topicsTextCode, { marginTop: 20 }]}>
          Successfully submitted.
        </Text>
      )}
      {submitted && (
        <Text style={[styles.topicsTextCode, { marginTop: 10 }]}>
          Your code is:
        </Text>
      )}
      {submitted && (
        <Text
          style={{
            fontFamily: "monospace",
            fontSize: 110,
            fontWeight: "bold",
            marginTop: 20,
            color: "black",
          }}
        >
          {code}
        </Text>
      )}
      {submitted && (
        <TouchableOpacity
          onPress={() => handleNewQuiz()}
          style={[styles.buttonCreate, { marginTop: 50 }]}
        >
          <Text
            style={{
              color: "white",
              fontFamily: "monospace",
              fontSize: 20,
              padding: 5,
            }}
          >
            New Survey
          </Text>
        </TouchableOpacity>
      )}
    </View>
  );
}

