import React, { useState } from "react";
import {
  View,
  Text,
  TouchableOpacity,
  TextInput,
  ScrollView,
  ActivityIndicator,
} from "react-native";
import { styles } from "../styles/ProfQuizStyles";
import { quizResults } from "./quizData";
